const fruits = ["Banana", "Apples", "Oranges", "Blueberries"];

//1. Remove Banana from the array.

fruits.shift();
console.log(fruits);

//2. Sort the array in alphabetical order.

fruits.sort();
console.log(fruits);

//3. Add Kiwi to the end

fruits.push("Kiwi");
console.log(fruits);

//4. Remove Apple From the Array

fruits.splice(0, 1);
console.log(fruits);

//5. Sort the array in reverse order

fruits.reverse();
console.log(fruits);
